// איסוף כלל רכיבי הכרטיסים מהמסמך ושמירתם ברשימה
const animalCards = document.querySelectorAll('.animal-card');

// איתור ושמירת אלמנט השמע המשמש כמוזיקת הרקע הכללית של האתר
const bgMusic = document.getElementById('bg-music');

// משתנה המשמש כנורית חיווי כדי לדעת אם מוזיקת הרקע כבר החלה להתנגן
let isBgMusicPlaying = false; 

/**
 * פונקציה מרכזית המקבלת את האות שנלחצה, מנגנת את הצליל המתאים ומפעילה את אנימציית הלחיצה
 */
function playSound(key) {
    // מציאת קובץ השמע המדויק ואת הכרטיס הרלוונטי על פי תכונת המידע המוגדרת בו
    const audio = document.getElementById(`sound-${key}`);
    const card = document.querySelector(`.animal-card[data-key="${key}"]`);

    // מנגנון הגנה: אם נלחץ מקש שאין לו חיה מתאימה, הפונקציה תעצור ולא תעשה כלום
    if (!audio || !card) return;

    /*
    * =====================================================================
    * דרישת התרגיל: אלמנט שלא נלמד בכיתה - איפוס זמן הניגון
    * =====================================================================
    * הסבר: מאפיין זה קובע את מיקום הניגון הנוכחי של קובץ השמע בשניות.
    * איפוס הערך לאפס טרם פקודת ההפעלה מאלץ את הקובץ להתחיל מיד מההתחלה.
    * פעולה זו מאפשרת השמעת הצליל שוב ושוב ברצף במקרה של לחיצות עוקבות ומהירות, 
    * מבלי שנצטרך להמתין עד שהניגון הקודם יסתיים לחלוטין.
    */
    audio.currentTime = 0; 
    audio.play();

    // הוספת מחלקה אקטיבית לכרטיס כדי שקובץ העיצוב יגדיל אותו ויאיר אותו
    card.classList.add('playing');

    // המתנה של מאה וחמישים מילישניות ולאחר מכן הסרת המחלקה כדי שהכרטיס יחזור לגודלו הרגיל
    setTimeout(() => {
        card.classList.remove('playing');
    }, 150);
}

/**
 * פונקציה שתפקידה להפעיל את שמע הרקע פעם אחת בלבד ברגע שהמשתמש מבצע פעולה ראשונה באתר
 * הדבר נדרש כי דפדפנים מודרניים חוסמים השמעת אודיו אוטומטית ללא אישור מהמשתמש
 */
function startBackgroundMusic() {
    if (!isBgMusicPlaying) {
        bgMusic.play();
        bgMusic.volume = 0.3; // החלשת מוזיקת הרקע כדי שקולות החיות יישמעו בבירור מעליה
        isBgMusicPlaying = true;
    }
}

// מעבר שיטתי על כל אחד מהכרטיסים והוספת מאזין המזהה לחיצות עכבר
animalCards.forEach(card => {
    card.addEventListener('click', function() {
        startBackgroundMusic(); // הפעלת מוזיקת הרקע במידה ועוד לא הופעלה
        
        // שליפת האות הספציפית השמורה בתוך הכרטיס שנלחץ והעברתה לפונקציית הניגון
        const key = this.getAttribute('data-key');
        playSound(key);
    });
});

// הוספת מאזין על כל המסמך שתפקידו לזהות מתי משתמש לוחץ על מקש במקלדת
document.addEventListener('keydown', function(event) {
    startBackgroundMusic(); // הפעלת מוזיקת הרקע במידה ועוד לא הופעלה
    
    // המרת האות שהוקלדה לאות קטנה כדי למנוע שגיאות במידה ומקש האותיות הגדולות דלוק
    const key = event.key.toLowerCase();
    playSound(key);
});